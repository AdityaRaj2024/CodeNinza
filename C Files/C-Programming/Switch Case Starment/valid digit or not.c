#include <stdio.h>
void main()
{
	int digit=1;
	switch(digit)
	{
		case 0:
			printf("Zero");
			break;
		case 1:
			printf("One");
			break;
		case 2:
			printf("Two");
			break;
		case 3:
			printf("Three");
			break;
		case 4:
			printf("Four");
			break;
		default:
			printf("Invalid Digit");
			break;
		
	}
	
	
	
	
	
	
	
}
